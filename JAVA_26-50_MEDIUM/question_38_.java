import java.util.Scanner;

public class question_38_ {
public static void main(String[] args) {
    
Scanner sc = new Scanner(System.in);

System.out.println("Enter Number");
int n =sc.nextInt();

int first = 0;
int secound = 1;

for(int i = 0; i<= n ;i++){

    System.out.print(first + " ");  // in first iteration it print 0 

    int next = first + secound ; // next = 0 + 1
    
    first = secound;   // first = 1  because secound value is 1
    secound = next ;  // and now secound = 1 because next is addation of first and secound 






}






}
 
}
