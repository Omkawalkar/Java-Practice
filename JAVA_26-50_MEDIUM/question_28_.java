public class question_28_ {
    public static void EvenNumber(int number) {
        for (int i = number; i <= 100; i += 2) {
            System.out.println(i);

        }
    }
//Print even numbers from 1 to 100.
    public static void main(String[] args) {

        int n = 2;
        EvenNumber(n);
    }
}
