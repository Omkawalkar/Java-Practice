public class question_6_ {
    public static void main(String[] args) {

        // Swap two numbers using a third variable.

        int a = 10;

        int b = 5;
        System.out.println("a is " + a + "\nb is " + b);
        int swap = a;
        a = b;
        b = swap;

        System.out.println("a is " + a + "\nb is " + b);

    }
}
